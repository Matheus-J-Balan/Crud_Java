    private void BtCadastrarActionPerformed(java.awt.event.ActionEvent evt) {
        String nome, data_nascimento, endereco, senha, confirmarSenha, login;
        nome = TFNome.getText();
        data_nascimento = TFDataNascimento.getText();
        endereco = TFEndereco.getText();
        login = TFLogin.getText();
        senha = String.valueOf(PFSenha.getPassword());
        confirmarSenha = String.valueOf(PFConfirmarSenha.getPassword());
        
        if (senha.equals(confirmarSenha)){
            Login log = new Login();
            log.nome = nome;
            log.data_nascimento = data_nascimento;
            log.endereco = endereco;
            log.login = login;
            log.senha = senha;
            
            CadastroDAO cadDAO = new CadastroDAO();
            cadDAO.salvarCadastro(log);
            
            BtVoltar.doClick();
        } else {
            JOptionPane.showMessageDialog(null, "A senha e a confirmação são diferentes!");
        }
        
    }

